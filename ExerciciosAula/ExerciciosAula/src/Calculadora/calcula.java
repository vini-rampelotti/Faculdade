package Calculadora;
public class calcula {
	
	public int soma (int x, int y) {
		return x + y;
	}
	public float soma (float x, float y) {
		float r = x + y;
		return (int) r;
	}
	public int subtracao (int x, int y) {
		return x - y;
	}
	public int multiplicacao (int x, int y) {
		return x * y;
	}
	public int divisao (int x, int y) {
		return x / y;
	}
	
}
